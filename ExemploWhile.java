package estruturaPosCondicionada;

public class ExemploWhile {

	public static void main(String[] args) {
		int i = 0;// define var i com o valor inicial de 0

		while(i<10) {
			i++;//soma +1, essa ação pode ser tomada no inicio e no fim do loop
			System.out.println(i);// se i++ exibe e depois soma 
		}
	}

}
